// ─────────────────────────────────────────────────────────────
//  LANGUAGE PROVIDER
// ─────────────────────────────────────────────────────────────

class LanguageProvider extends InheritedWidget {
  final AppLanguage currentLanguage;
  final void Function(AppLanguage) setLanguage;

  const LanguageProvider({super.key, required this.currentLanguage,
      required this.setLanguage, required super.child});

  static LanguageProvider of(BuildContext context) =>
      context.dependOnInheritedWidgetOfExactType<LanguageProvider>()!;

  String t(String key) => AppTranslations.get(key, currentLanguage);

  @override
  bool updateShouldNotify(LanguageProvider old) =>
      currentLanguage != old.currentLanguage;
}

// ─────────────────────────────────────────────────────────────
//  LANGUAGE ROOT
// ─────────────────────────────────────────────────────────────

class LanguageRoot extends StatefulWidget {
  final Widget Function(BuildContext, AppLanguage) builder;
  const LanguageRoot({super.key, required this.builder});

  @override
  State<LanguageRoot> createState() => _LanguageRootState();
}

class _LanguageRootState extends State<LanguageRoot> {
  AppLanguage _lang = AppLanguage.english;
  void _set(AppLanguage lang) => setState(() => _lang = lang);

  @override
  Widget build(BuildContext context) => LanguageProvider(
        currentLanguage: _lang,
        setLanguage: _set,
        child: widget.builder(context, _lang),
      );
}

// ─────────────────────────────────────────────────────────────
//  LANGUAGE PICKER SHEET
// ─────────────────────────────────────────────────────────────

class LanguagePickerSheet extends StatelessWidget {
  const LanguagePickerSheet({super.key});

  static void show(BuildContext context) => showModalBottomSheet(
        context: context,
        isScrollControlled: true,
        shape: const RoundedRectangleBorder(
            borderRadius: BorderRadius.vertical(top: Radius.circular(20))),
        builder: (_) => const LanguagePickerSheet(),
      );

  @override
  Widget build(BuildContext context) {
    final provider = LanguageProvider.of(context);
    return DraggableScrollableSheet(
      initialChildSize: 0.6, maxChildSize: 0.9, minChildSize: 0.4,
      expand: false,
      builder: (_, ctrl) => SafeArea(
        child: Column(children: [
          Padding(
            padding: const EdgeInsets.fromLTRB(16, 16, 16, 8),
            child: Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
              Center(child: Container(width: 40, height: 4,
                  decoration: BoxDecoration(color: Colors.grey.shade300,
                      borderRadius: BorderRadius.circular(2)))),
              const SizedBox(height: 16),
              Text(provider.t('select_language'),
                  style: const TextStyle(fontSize: 18, fontWeight: FontWeight.w700)),
            ]),
          ),
          Expanded(child: ListView(controller: ctrl,
            children: AppTranslations.allLanguages.map((lang) {
              final info = AppTranslations.languageInfo[lang]!;
              final selected = provider.currentLanguage == lang;
              return ListTile(
                leading: Text(info.flag, style: const TextStyle(fontSize: 28)),
                title: Text(info.nativeName,
                    style: TextStyle(
                        fontWeight: selected ? FontWeight.w700 : FontWeight.w500,
                        color: selected ? const Color(0xFF2F7F34) : const Color(0xFF1E293B))),
                subtitle: Text(info.name,
                    style: TextStyle(color: Colors.grey.shade500, fontSize: 12)),
                trailing: selected
                    ? const Icon(Icons.check_circle_rounded, color: Color(0xFF2F7F34))
                    : null,
                onTap: () { provider.setLanguage(lang); Navigator.pop(context); },
              );
            }).toList(),
          )),
        ]),
      ),
    );
  }
}